package com.beans;

public class Team {

	private int id;
	private String name;
	private String introduction;
	private String memberid;
	private int membermun;
	private String projectid;
	private int projectmun;
	private String foundtime;
	private int captainid;
	
	
	
	public int getCaptainid() {
		return captainid;
	}
	public void setCaptainid(int captainid) {
		this.captainid = captainid;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIntroduction() {
		return introduction;
	}
	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}
	public String getMemberid() {
		return memberid;
	}
	public void setMemberid(String memberid) {
		this.memberid = memberid;
	}
	public int getMembermun() {
		return membermun;
	}
	public void setMembermun(int membermun) {
		this.membermun = membermun;
	}
	public String getProjectid() {
		return projectid;
	}
	public void setProjectid(String projectid) {
		this.projectid = projectid;
	}
	public int getProjectmun() {
		return projectmun;
	}
	public void setProjectmun(int projectmun) {
		this.projectmun = projectmun;
	}
	public String getFoundtime() {
		return foundtime;
	}
	public void setFoundtime(String foundtime) {
		this.foundtime = foundtime;
	}
	
	
	
	
}
